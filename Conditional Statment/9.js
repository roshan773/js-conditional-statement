// Day schedule with the switch case [with break]

var option = 3;
switch(option){
    case 1:{
        console.log("Day 1 of work");
        break;
    }
    case 2:{
        console.log("Day 2 of work");
        break;
    }
    case 3:{
        console.log("Day 3 of work");
        break;
    }
    case 4:{
        console.log("Holiday");
        break;
    }
}